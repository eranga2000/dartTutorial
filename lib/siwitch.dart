void usingSwitcgh() {
  String fruit = 'apple';
  switch (fruit) {
    case 'apple':
      print("it is an apple,it is red");
    case 'orange':
      print('it is an orange it is orange in color');

    case 'woodApple':
      print('woodapple is brown and it has a hard shell');
    default:
      print('it is a $fruit');
  }
}
